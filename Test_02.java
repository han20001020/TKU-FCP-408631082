public class Test_02 {
    public static void main(String[] args) {
        // 宣告並賦值攝氏溫度
        double celsius = 25.0;

        // 使用公式計算華氏溫度
        double fahrenheit = (celsius * 9 / 5) + 32;

        // 使用System.out.println()方法輸出結果
        System.out.println("攝氏溫度 " + celsius + " 度轉換為華氏溫度是 " + fahrenheit + " 度。");
    }
}
